        entrada = entrada.lower()
        palavras = entrada.split()
        palavras_invalidas = []

        if len(palavras) == 1:
            print("COMPARANDO PALAVRA COM A GRAMTICA")
        else: 
            print("COMPARANDO PALAVRAS COM A GRAMTICA")
            
        for palavra in palavras:
            print()
            if  len(palavra) > 1:
                print(f"Token: {palavra.capitalize()}")
                self.estado_atual = self.estado_inicial
                palavra_valida = True
            else:
                print(f"Token: {palavra}")
                self.estado_atual = self.estado_inicial
                palavra_valida = True

            for letra in palavra:
                if letra not in self.alfabeto:
                    palavra_valida = False
                    print(f"Token invlido: {letra}")
                    break
                else:
                    print(f"Estado atual: {self.estado_atual}")
                    self.estado_atual = self.funcao_transicao.get((self.estado_atual, letra), None)
                    if self.estado_atual is None:
                        palavra_valida = False
                        print("Transio invlida encontrada.")
                        break
            
            if palavra_valida and self.estado_atual in self.estados_finais:
                print(f"Estado final: {self.estado_atual}")
            else:
                palavras_invalidas.append(palavra)

        if palavras_invalidas:
            if len(palavras_invalidas) == 1:
                print(f"A palavra '{palavras_invalidas[0]}' no pertence  gramtica")
            else:
                palavras_str = ', '.join([f"'{p}'" for p in palavras_invalidas])
                print(f"As palavras {palavras_str} no pertencem  gramtica")
        else:
            if len(palavras) == 1:
                print("A palavras pertence  gramtica")
            else:      
                print("Todas as palavras pertencem  gramtica")
                
